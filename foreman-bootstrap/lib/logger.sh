#!/bin/bash
###############################################################################
# logger.sh
#
# Logging library for Foreman Bootstrap
###############################################################################

mkdir -p "$(dirname "$LOG_FILE")" 2>/dev/null

###############################################################################
# Timestamp
###############################################################################

timestamp() {
    date "+%Y-%m-%d %H:%M:%S"
}

###############################################################################
# Internal logger
###############################################################################

_write_log() {

    local LEVEL="$1"
    local COLOR="$2"
    local MESSAGE="$3"

    echo -e "${COLOR}[$(timestamp)] [$LEVEL] ${MESSAGE}${NC}"

    if [[ -n "${LOG_FILE:-}" ]]; then
        echo "[$(timestamp)] [$LEVEL] ${MESSAGE}" >> "$LOG_FILE"
    fi
}

###############################################################################
# Info
###############################################################################

log_info() {
    _write_log "INFO" "$CYAN" "$1"
}

###############################################################################
# Success
###############################################################################

log_success() {
    _write_log " OK " "$GREEN" "$1"
}

###############################################################################
# Warning
###############################################################################

log_warn() {
    _write_log "WARN" "$YELLOW" "$1"
}

###############################################################################
# Error
###############################################################################

log_error() {
    _write_log "FAIL" "$RED" "$1"
}

###############################################################################
# Step
###############################################################################

log_step() {

    echo
    echo "--------------------------------------------------------"

    _write_log "STEP" "$BLUE" "$1"

    echo "--------------------------------------------------------"
}

###############################################################################
# Header
###############################################################################

header() {

echo
echo "========================================================"
echo "$1"
echo "========================================================"
echo

}

###############################################################################
# Banner
###############################################################################

banner() {

cat <<'EOF'

###############################################################
#                                                             #
#          FOREMAN / KATELLO BOOTSTRAP INSTALLER              #
#                                                             #
#                 PXE + Katello Automation                    #
#                                                             #
###############################################################

EOF

}

###############################################################################
# Progress
###############################################################################

progress() {

    local CURRENT="$1"
    local TOTAL="$2"

    local WIDTH=40

    local FILLED=$(( CURRENT * WIDTH / TOTAL ))
    local EMPTY=$(( WIDTH - FILLED ))

    printf "["

    for ((i=0;i<FILLED;i++))
    do
        printf "#"
    done

    for ((i=0;i<EMPTY;i++))
    do
        printf "-"
    done

    printf "] %d%%\r" $(( CURRENT * 100 / TOTAL ))

    if [[ "$CURRENT" -eq "$TOTAL" ]]; then
        echo
    fi
}

###############################################################################
# Execute Command
###############################################################################

run_cmd() {

    local CMD="$1"

    log_info "$CMD"

    if eval "$CMD" >>"$LOG_FILE" 2>&1
    then
        log_success "Completed"
        return 0
    else
        log_error "Command Failed"

        echo
        echo "Command:"
        echo "$CMD"
        echo

        exit 1
    fi
}

###############################################################################
# Exit Handler
###############################################################################

finish() {

    local RC=$?

    if [[ $RC -eq 0 ]]
    then
        log_success "Bootstrap completed successfully."
    else
        log_error "Bootstrap exited with status $RC."
    fi
}

trap finish EXIT
